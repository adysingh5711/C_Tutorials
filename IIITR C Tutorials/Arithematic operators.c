#include <stdio.h>
int main()
{
	int a,b;
	printf("Enter a \n");
	scanf("%d", &a);
	printf("Enter b \n");
	scanf("%d", &b);
	printf("Addition is :%d \n",a+b);
	printf("Subtraction is :%d \n",a-b);
	printf("Multiplication is :%d \n",a*b);
	printf("Division is :%d \n",a/b);
	printf("Modulus is :%d \n",a%b);
	return 0;
}
